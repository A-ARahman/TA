    {
        $queryBuilder = $this->getQueryBuilder();
        $queryBuilder->delete($this->UpdateTable);
        if (is_array($where)) {
            $where = $this->arrayToFilter($where);
        }
        if ($rs) {
            if (array_key_exists('data_id', $rs)) {
                AddFilter($where, QuotedName('data_id', $this->Dbid) . '=' . QuotedValue($rs['data_id'], $this->data_id->DataType, $this->Dbid));
